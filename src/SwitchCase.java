public class SwitchCase {
    public static void main(String[] args) {
        int noOfDay = 4;
        switch (noOfDay){
            case 1:
                System.out.println("Day is sunday.");
                break;
            case 2:
                System.out.println("Day is monday.");
                break;
            case 3:
                System.out.println("Day is tuesday");
                break;
            default:
                System.out.println("Invalid option.");
        }
    }
}
