package PastWork;

public class MathWork {
    public static void main(String[] args) {
        System.out.println(Math.max(10,50)); // Max
        System.out.println(Math.min(10,50)); // Min
        System.out.println(Math.round(25.4));
        System.out.println(Math.floor(25.9));
        System.out.println(Math.ceil(25.1)); //

        double random = Math.random() * (10 - 1 + 1) + 1;
        System.out.println(random);
    }
}
